def _html_block(n):
    t = n["type"]
    if t == "document":
        return "\n".join(_html_block(c) for c in n["children"])
    if t == "metadata":
        return f"<!-- {n.get('format','yaml')} metadata:\n{n['text']}\n-->"
    if t == "heading":
        return f"<h{n['level']}>{_html_inline(n['children'])}</h{n['level']}>"
    if t == "paragraph":
        return f"<p>{_html_inline(n['children'])}</p>"
    if t == "thematic_break":
        return "<hr>"
    if t == "code_block":
        cls = f' class="language-{n["lang"]}"' if n.get("lang") else ""
        return f"<pre><code{cls}>{_esc(n['text'])}</code></pre>"
    if t == "latex_block":
        return f"<pre><code class=\"language-latex\">{_esc(n['text'])}</code></pre>"
    if t == "blockquote":
        inner = "\n".join(_html_block(c) for c in n["children"])
        return f"<blockquote>\n{inner}\n</blockquote>"
    if t == "list":
        tag = "ol" if n.get("ordered") else "ul"
        items = "\n".join(_html_li(it) for it in n["children"])
        return f"<{tag}>\n{items}\n</{tag}>"
    if t == "table":
        return _html_table(n)
    return _html_inline(n.get("children", []))


def _html_li(it):
    body = "".join(_html_block_inlineable(c) for c in it["children"])
    if it.get("is_checked") == "checked":
        body = '<input type="checkbox" checked> ' + body
    elif it.get("is_checked") == "unchecked":
        body = '<input type="checkbox"> ' + body
    return f"<li>{body}</li>"


def _html_block_inlineable(c):
    # a single paragraph inside <li> renders inline (no <p>) for compactness
    if c["type"] == "paragraph":
        return _html_inline(c["children"])
    return _html_block(c)


def _html_table(n):
    rows = n["children"]
    if not rows:
        return "<table></table>"
    out = ["<table>"]
    for r in rows:
        cell_tag = "th" if r.get("header") else "td"
        cells = "".join(f"<{cell_tag}>{_html_inline(c['children'])}</{cell_tag}>"
                        for c in r["children"])
        out.append(f"<tr>{cells}</tr>")
    out.append("</table>")
    return "\n".join(out)


def _html_inline(nodes):
    out = []
    for n in nodes:
        t = n["type"]
        if t == "text":
            out.append(_esc(n["text"]))
        elif t == "strong":
            out.append("<strong>" + _html_inline(n["children"]) + "</strong>")
        elif t == "italic":
            out.append("<i>" + _html_inline(n["children"]) + "</i>")
        elif t == "emphasis":
            out.append("<em>" + _html_inline(n["children"]) + "</em>")
        elif t == "strikethrough":
            out.append("<del>" + _html_inline(n["children"]) + "</del>")
        elif t == "code_span":
            out.append("<code>" + _esc(n["text"]) + "</code>")
        elif t == "link":
            url = _esc(n.get("url", ""))
            title = _esc(n.get("title", ""))
            text =  _esc(n.get("text", ""))
            if title:
                out.append(f'<a href="{url}" title="{title}">{text}</a>')
            else:
                out.append(f'<a href="{url}">{text}</a>')
        elif t == "image":
            text = _esc(n["text"])
            out.append(f'<img src="{_esc(n["url"])}"> {text} </img>')
        elif t == "wikilink":
            url = n.get("url", "")
            text = n.get("text", "")
            if text:
                out.append(f"[[{url}|{text}]]")
            else:
                out.append(f"[[{url}]]")
        elif t == "embed":
            url = n.get("url", "")
            text = n.get("text", "")
            if text:
                out.append(f"![[{url}|{text}]]")
            else:
                out.append(f"![[{url}]]")
        elif t == "hardbreak":
            out.append("<br>\n")
        # elif t == "softbreak":
        #     out.append("\n")
        else:
            out.append(_esc(n.get("text", "")))
    return "".join(out)


# ===========================================================================
# latex block post-processing: merge $$...$$ paragraph sequences
# ===========================================================================
def dump_html(node):
    return "\n".join(_html_block(c) for c in node.get("children", [])).rstrip("\n") + "\n"


