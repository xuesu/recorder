"""
Markdown <-> HTML via a middle JSON tree, using tree-sitter for parsing.
Generated by Claude Opus 4.8 High

Middle node shape (plain dicts):
    {"type": "...", "children": [...], "text": "...", ...extra fields}

Block types:    document, metadata(format), heading(level), paragraph,
                blockquote, list(ordered,start), list_item(is_checked), code_block(lang),
                thematic_break, table, table_row(header), table_cell
Inline types:   text, italic, strong, emphasis, strikethrough, code_span,
                link(url,text), image(url,text), embed(url, text), wikilink(url, text), softbreak(I didn't detect the real usage of this rule), hardbreak

Here to simplify the structure: 
    link: `[text](url)`, if text isn't set, text = url
    image: `![text](url)`, if text isn't set, text = url
    wikilink: `[[url|text]]`, if text isn't set, `[[url]]`
    embed: `![[url|text]]`, if text isn't set, `![[url]]`
    list: only `-`
    table: only `|---|...|---|`
    
Public API:
    parse_markdown(md)  -> middle      (tree-sitter; GFM extensions)
        - Current tradeoffs:
            1. some elements that allow multi-lines(without the need of double newlines) including latex_block, blockquote, I would treat the whole block as a paragraph with "\n" inside its text node. Somtimes those blocks would be fused into other paragraph.
                - For normally I won't edit those kinds of blocks 
                - however, the "\n" won't be visible from html or from the console of browser, the important newlines only existed in the html source code. We keep our scenes restricted so right now we use the html source code to save those data.
                - In the future, I might set a flag to show if a paragraph can be parsed into those block or if any of these blocks need to split inner paragraph containing "\n" into multiple paragraphs, we then also need to parse those paragraphs back and remove the additional lines.
            2. comments, references are treated as visible paragraphs instead of invisible special elements to avoid missing them and to get better conversion.
            3. softbreak: I didn't detect the usage of softbreak inside treesitter-markdown, so I directly throw a ValueError
            4. block_continuation: block that needd block_continuation, like backquote, might wrongly distributed the startbyte and endbyte of a node. we use _txt_by_lines to backsearch few bytes until newline since multi line blocks always startswith newline and endswith newline.
            5. 
    parse_html(html)    -> middle      (tree-sitter)
        - Current tradeoffs:
            A: from html generated via formats.py from markdown
            B: from html generated by uploading markdown to slack canvas then downloading
    dump_markdown(node) -> md          (pure)
        - Current tradeoffs:
    dump_html(node)     -> html        (pure)
        - Current tradeoffs:
    markdown_to_html(md)
    
    html_to_markdown(html)

NOTE: tree-sitter-markdown ships the GFM extensions (task lists, strikethrough, pipe tables) and minus/plus metadata by default.
"""

