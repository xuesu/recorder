def _md_blocks(nodes, parent_type, depth):
    parts = []
    for node in nodes:
        text = _md_block(node, parent_type, depth)
        if text is None:
            continue
        if parent_type in {"paragraph", "document"} and node["type"] in {"paragraph"} \
                or parent_type not in {"list"} and node["type"] in {"list"}:
            if len(text) == 0 and len(parts) > 0:
                continue
            if not text.endswith("\n") and not text.endswith("  ") and not text.endswith("\\"):
                text += "\n"
        if node["type"] == "document":
            snode = node["children"][0]
        else:
            snode = node
        if snode["type"] == "heading" and (len(parts) > 0 and not parts[-1].endswith("\n") and not len(parts[-1]) == 0):
            text = "\n" + text
        parts.append(text)

    if parent_type == "code_block":
        return "\n".join(parts)
    else:
        return "\n".join(p for p in parts if len(p) > 0)


def _md_block(n, parent_type, depth):
    t = n["type"]
    if t == "document":
        return _md_blocks(n["children"], t, depth)
    if t == "metadata":
        fence = "---" if n.get("format") == "yaml" else "+++"
        return f"{fence}\n{n['text']}\n{fence}"
    if t == "heading":
        return "#" * n["level"] + " " + _md_inline(n["children"])
    if t == "paragraph":
        return _md_inline(n["children"])
    if t == "thematic_break":
        return "---"
    if t == "code_block":
        return f"```{n.get('lang','')}\n{n['text']}\n```"
    if t == "latex_block":
        return f"$$\n{n['text']}\n$$"
    if t == "blockquote":
        inner_lines = _md_blocks(n["children"], t, 0).split("\n")
        parts = []
        for ln in inner_lines:
            if ln and ln != ">" and not (ln.startswith(">") and ln.lstrip(">")[0] == " "):
                parts.append("> " + ln)
            else:
                parts.append(">" + ln)
        return "\n".join(parts)
    if t == "list":
        return _md_list(n, depth)
    if t == "table":
        return _md_table(n)
    return _md_inline(n.get("children", [])) if "children" in n else n.get("text", "")


def _md_list(n, depth):
    items = []
    ordered = n.get("ordered", False)
    num = n.get("start", 1)
    for item in n["children"]:
        assert item["type"] == "list_item"
        if len(item["children"]) == 1 and item["children"][0]["type"] == "list":
            items.append(_md_blocks(
                item["children"], item["type"], depth + 1).rstrip())
        else:
            marker = (f"{num}. " if ordered else "- ")
            if item.get("is_checked") == "checked":
                marker += "[x] "
            elif item.get("is_checked") == "unchecked":
                marker += "[ ] "
            body = _md_blocks(item["children"], item["type"], depth + 1)
            item_lines = body.split("\n")
            first_text = marker + _md_blocks(item["children"][:1], item["type"], depth + 1)
            print("is first text too large?", first_text, "\nitem", item)
            first_line = marker + item_lines[0]
            rest = [("    " + ln if ln else "") for ln in item_lines[first_line.count("\n") + 1:]]
            print("rest", rest, n)
            items.append("\n".join([first_line] + rest).strip())
        num += 1
    block = "\n".join(items)
    return block


def _md_table(n):
    rows = n["children"]
    if not rows:
        return ""
    align = n.get("align") or [None] * len(rows[0]["children"])

    def render_row(cells):
        return "| " + " | ".join(_md_inline(c["children"]) for c in cells) + " |"

    def delim(header):
        return "-" * len(header) if len(header) > 0 else "--"

    header = rows[0]
    out = [render_row(header["children"])]
    headers = out[0][1:-1].split("|")
    out.append("|" + "|".join(delim(header) for header in headers) + "|")
    for r in rows[1:]:
        out.append(render_row(r["children"]))
    return "\n".join(out)


def _md_inline(nodes):
    out = []
    for n in nodes:
        t = n["type"]
        if t == "text":
            out.append(n["text"])
        elif t == "emphasis":
            out.append("*" + _md_inline(n["children"]) + "*")
        elif t == "strong":
            out.append("**" + _md_inline(n["children"]) + "**")
        elif t == "italic":
            out.append("*" + _md_inline(n["children"]) + "*")
        elif t == "strikethrough":
            out.append("~~" + _md_inline(n["children"]) + "~~")
        elif t == "code_span":
            out.append("`" + n["text"] + "`")
        elif t == "link":
            text = n.get("text", "")
            url = n.get("url", "")
            if text == url or not text:
                out.append(url)
            else:
                out.append(f"[{text}]({url})")
        elif t == "image":
            text = n.get("text", "")
            out.append(f"![{text}]({n['url']})")
        elif t == "wikilink":
            url = n.get("url", "")
            text = n.get("text", "")
            if text:
                out.append(f"[[{url}|{text}]]")
            else:
                out.append(f"[[{url}]]")
        elif t == "embed":
            url = n.get("url", "")
            text = n.get("text", "")
            if text:
                out.append(f"![[{url}|{text}]]")
            else:
                out.append(f"![[{url}]]")
        elif t == "hardbreak":
            out.append("  \n")
        # elif t == "softbreak":
        #     out.append("\n")
        else:
            out.append(n.get("text", ""))
    return "".join(out)


def dump_markdown(node):
    return _md_blocks(node.get("children", []), node["type"], 0).rstrip("\n") + "\n"


