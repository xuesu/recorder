def dump_treesitter_tree(node, src: bytes, depth=0):
    text = src[node.start_byte:node.end_byte].decode("utf-8", "replace")
    snippet = text if len(text) <= 40 else text[:37] + "..."
    print("  " * depth + f"{node.type}  "
          f"[{node.start_point}-{node.end_point}]  "
          f"named={node.is_named}  text={snippet!r}")
    for child in node.children:
        dump_treesitter_tree(child, src, depth + 1)

def merge_text(nodes):
    merged = []
    for n in nodes:
        if (n["type"] == "text" and merged and merged[-1]["type"] == "text"):
            merged[-1]["text"] += n["text"]
        else:
            merged.append(n)
    return merged

def recover_abnormal_children(f):
    def wrap(*args, **kwargs):
        midnode = f(*args, **kwargs)
        if midnode is not None:
            for c in list(midnode.get("children", [])):
                if "abnormal_children" in c:
                    midnode["children"] += c["abnormal_children"]
                    c.pop("abnormal_children")
        return midnode
    return wrap

def parse_markdown(text):
    _, block_lang, inline_lang = _languages()
    block_parser = _make_parser(block_lang)
    inline_parser = _make_parser(inline_lang)
    src = text.encode("utf-8")
    tree = block_parser.parse(src)
    dump_treesitter_tree(tree.root_node, src)  # debug
    
    def _txt_by_lines(node, subsrc):
        # only workable in markdown
        start_byte = node.start_byte
        end_byte = node.end_byte
        while start_byte > 0 and subsrc[start_byte] != 0x0a:
            start_byte -= 1
        while end_byte > start_byte and subsrc[end_byte - 1] == 0x20:
            end_byte -= 1
        while end_byte < len(subsrc) and subsrc[end_byte - 1] != 0x0a:
            end_byte += 1
        return _txt2(start_byte, end_byte, subsrc)

    def parse_inline(text):
        b = text.encode("utf-8")
        itree = inline_parser.parse(b)
        dump_treesitter_tree(itree.root_node, b)
        return _inline_children(itree.root_node, b)

    def parse_inline_blocks(text):
        b = text.encode("utf-8")
        itree = block_parser.parse(b)
        dump_treesitter_tree(itree.root_node, b)
        children = [block(c, subsrc=b) for c in itree.root_node.children if _is_block(c)]
        return children

    def _inline_children(node, b):
        out = []
        pos = node.start_byte
        children = list(node.children)
        i = 0
        while i < len(children):
            ch = children[i]
            # Detect [[wikilink]]: text "[" + shortcut_link + text "]"
            if (ch.type == "text" and _txt(ch, b) == "["
                    and i + 2 < len(children)
                    and children[i + 1].type == "shortcut_link"
                    and children[i + 2].type == "text" and _txt(children[i + 2], b) == "]"):
                if ch.start_byte > pos:
                    text = b[pos:ch.start_byte].decode("utf-8")
                    out.append({"type": "text", "text": text})
                lt = _txt(next(c for c in children[i + 1].children if c.type == "link_text"), b)
                parts = lt.split("|", 1)
                out.append({"type": "wikilink", "url": parts[0], "text": parts[1] if len(parts) > 1 else ""})
                pos = children[i + 2].end_byte
                i += 3
                continue
            if ch.start_byte > pos:
                text = b[pos:ch.start_byte].decode("utf-8")
                out.append({"type": "text", "text": text})
            out.extend(_inline_node(ch, b))
            pos = ch.end_byte
            i += 1
        if pos < node.end_byte:
            text = b[pos:node.end_byte].decode("utf-8")
            out.append({"type": "text", "text": text})
        if not node.children and node.type == "inline":
            return [{"type": "text", "text": _txt(node, b)}]
        return merge_text(out)

    def _inline_node(n, b):
        t = n.type
        if t in ("emphasis_delimiter", "code_span_delimiter"):
            return []
        if t in ("italic", "emphasis", "strong_emphasis"):
            mapped = {"emphasis": "emphasis", "strong_emphasis": "strong",
                      "italic": "italic"}[t]
            return [{"type": mapped, "children": _inline_children(n, b)}]
        if t == "strikethrough":
            non_delim = [c for c in n.children
                         if c.type not in ("emphasis_delimiter",)]
            if len(non_delim) == 1 and non_delim[0].type == "strikethrough":
                return [{"type": "strikethrough",
                         "children": _inline_children(non_delim[0], b)}]
            return [{"type": "strikethrough", "children": _inline_children(n, b)}]
        if t == "code_span":
            text = _txt(n, b)
            assert text.startswith("`") and text.endswith("`")
            return [{"type": "code_span", "text": text[1:-1]}]
        if t in ("inline_link", "link"):
            text = url = ""
            for c in n.children:
                if c.type in ("link_text",):
                    text = _txt(c, b)
                elif c.type == "link_destination":
                    url = _txt(c, b)
                # elif c.type == "link_title":
                #     title = _txt(c, b).strip("\"'")
            return [{"type": "link", "url": url, "text": text}]
        if t == "image":
            url = ""
            has_link_dest = False
            for c in n.children:
                # We deliberately set text as alt
                if c.type == "image_description":
                    text = _txt(c, b)
                if c.type == "link_destination":
                    url = _txt(c, b)
                    has_link_dest = True
                # elif c.type == "link_title":
                #     title = _txt(c, b).strip("\"'")
            # Obsidian embed ![[...]] has no link_destination
            if not has_link_dest:
                for c in n.children:
                    if c.type == "image_description":
                        for cc in c.children:
                            if cc.type == "shortcut_link":
                                lt_node = next((x for x in cc.children if x.type == "link_text"), None)
                                if lt_node:
                                    lt = _txt(lt_node, b)
                                    parts = lt.split("|", 1)
                                    return [{"type": "embed", "url": parts[0], "text": parts[1] if len(parts) > 1 else ""}]
                return [{"type": "text", "text": _txt(n, b)}]
            return [{"type": "image", "url": url, "text": text}]
        if t in ("hard_line_break",):
            return [{"type": "hardbreak"}]
        if t in ("soft_line_break",):
            raise ValueError("thought soft_line_break is dead!")
            # return [{"type": "softbreak"}]
        # leaf text / anything unhandled -> raw text
        if n.child_count == 0:
            return [{"type": "text", "text": _txt(n, b)}]
        return _inline_children(n, b)


    @recover_abnormal_children
    def block(node, subsrc):
        t = node.type
        if t in (MD["heading"], MD["setext"]):
            level = 1
            for c in node.children:
                if c.type.startswith("atx_h") and c.type.endswith("_marker"):
                    level = c.type.count("h") and int(
                        c.type[5])  # atx_h3_marker
                if c.type == "setext_h1_underline":
                    level = 1
                if c.type == "setext_h2_underline":
                    level = 2
            inline = next(
                (c for c in node.children if c.type == MD["inline"]), None)
            kids = parse_inline(_txt(inline, subsrc)) if inline else []
            return {"type": "heading", "level": level, "children": kids}
        if t == MD["paragraph"]:
            inline = next(
                (c for c in node.children if c.type == MD["inline"]), None)
            kids = parse_inline(_txt(inline, subsrc)) if inline else parse_inline(_txt(node, subsrc))
            return {"type": "paragraph", "children": kids}
        if t == MD["blockquote"]:
            lines = []
            # treesitter bug
            abnormal_lines = [] # treesitter attached siblings into that block wrongly
            for line in _txt(node, subsrc).splitlines():
                if len(abnormal_lines) == 0 and line.startswith(">"):
                    lines.append(line[1:].lstrip())
                else:
                    abnormal_lines.append(line)
            children = parse_inline_blocks("\n".join(lines))
            res = {"type": "blockquote", "children": children}
            if len(abnormal_lines) > 0:
                res["abnormal_children"] = parse_inline_blocks("\n".join(abnormal_lines))
            return res
        if t == MD["list"]:
            items = [c for c in node.children if c.type == MD["list_item"]]
            ordered = False
            start = 1
            for it in items:
                for c in it.children:
                    if c.type.startswith("list_marker"):
                        mk = _txt(c, subsrc).strip()
                        if mk[:1].isdigit():
                            ordered = True
                            start = int(
                                "".join(ch for ch in mk if ch.isdigit()) or 1)
                        break
                break
            return {"type": "list", "ordered": ordered, "start": start,
                    "children": [list_item(it, subsrc=subsrc) for it in items]}
        if t == MD["fenced"]:
            lines = _txt_by_lines(node, subsrc).lstrip('\n').splitlines()
            assert lines[0].strip().startswith("```") and lines[-1].strip().startswith('```')
            level = lines[0].index('`') // 4
            for line in lines[1:-1]:
                assert line.startswith("    " * level)
            lang = lines[0].strip().lstrip("`").strip()
            code = "\n".join(line[4*level:] for line in lines[1:-1])
            return {"type": "code_block", "lang": lang, "level": level, "text": code.rstrip("\n")}
        if t == MD["indented"]:
            return {"type": "code_block", "lang": "",
                    "text": _txt(node, subsrc).rstrip("\n")}
        if t == MD["table"]:
            return parse_table(node, subsrc=subsrc)
        if t == MD["thematic"]:
            return {"type": "thematic_break"}
        if t == MD["yaml_meta"]:
            return {"type": "metadata", "format": "yaml",
                    "text": _txt(node, subsrc).strip().strip("-").strip()}
        if t == MD["toml_meta"]:
            return {"type": "metadata", "format": "toml",
                    "text": _txt(node, subsrc).strip().strip("+").strip()}
        if t in ("link_reference_definition",):
            return {"type": "paragraph", "children": [{"type": "text", "text": _txt(node, subsrc)}]}
        # containers like 'section' -> descend
        kids = [block(c, subsrc=subsrc) for c in node.children if _is_block(c)]
        kids = [k for k in kids if k]
        if len(kids) == 1:
            return kids[0]
        return {"type": "document", "children": kids} if kids else None

    def _is_block(c):
        return c.type in set(MD.values()) | {"section"} or c.type.endswith("_block") \
            or c.type in ("list_item", "link_reference_definition")

    def list_item(it, subsrc):
        is_checked = None
        for c in it.children:
            if c.type == "task_list_marker_checked":
                is_checked = "checked"
            elif c.type == "task_list_marker_unchecked":
                is_checked = "unchecked"
        kids = [block(c, subsrc) for c in it.children if _is_block(c)]
        kids = [k for k in kids if k]
        return {"type": "list_item", "is_checked": is_checked, "children": kids}

    def parse_table(node, subsrc):
        rows = []
        align = []
        for c in node.children:
            if c.type == "pipe_table_header":
                rows.append(table_row(c, header=True, subsrc=subsrc))
            elif c.type == "pipe_table_row":
                rows.append(table_row(c, header=False, subsrc=subsrc))
            elif c.type == "pipe_table_delimiter_row":
                for d in c.children:
                    if d.type == "pipe_table_delimiter_cell":
                        s = _txt(d, subsrc).strip()
                        left, right = s.startswith(":"), s.endswith(":")
                        align.append("center" if left and right else
                                     "right" if right else
                                     "left" if left else None)
        return {"type": "table", "align": align, "children": rows}

    def table_row(c, header, subsrc):
        cells = []
        for cell in c.children:
            if cell.type == "pipe_table_cell":
                inline = _txt(cell, subsrc)
                cells.append({"type": "table_cell",
                              "children": parse_inline(inline)})
        return {"type": "table_row", "header": header, "children": cells}

    root = tree.root_node
    children = [block(c, src) for c in root.children if _is_block(c)]
    children = [c for c in children if c is not None]
    result = {"type": "document", "children": children}
    return result


