def _make_parser(lang):
    try:
        return tree_sitter.Parser(lang)                 # tree_sitter >= 0.22
    except TypeError:
        p = tree_sitter.Parser()
        if hasattr(p, "set_language"):
            p.set_language(lang)            # tree_sitter < 0.22
        else:
            p.language = lang
        return p


def _languages():
    html_lang = tree_sitter.Language(tree_sitter_html.language())
    block_lang = tree_sitter.Language(tree_sitter_markdown.language())
    inline_lang = tree_sitter.Language(tree_sitter_markdown.inline_language())
    return html_lang, block_lang, inline_lang


def _txt(node, src):
    return _txt2(node.start_byte, node.end_byte, src)

def _txt2(start_byte, end_byte, src):
    return src[start_byte:end_byte].decode("utf-8").replace("\u00a0", " ").replace("\u2014", "-").replace("\u2026", "-")

def _esc(s):
    return html.escape(s, quote=False)

def text_only4node(node):
    if "children" not in node:
        return _md_inline([node])
    return _md_inline(node["children"])

